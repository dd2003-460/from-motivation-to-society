# Extract topic from subsubsection title
def get_topic(line):
    m = re.search(r'\\subsubsection\*\{段落\d+：(.+)\}', line)
    return m.group(1) if m else None

def generate_prediction(topic):
    if topic in DEFAULT_PREDICTIONS:
        return DEFAULT_PREDICTIONS[topic]
    # Fallback generic
    return f"你可能会想：'{topic}'这个说法是不是太抽象了？\\ 但这里的核心洞见是：当你理解了背后的机制，抽象就变成了具体工具。"

def process_file(filepath):
    lines = open(filepath, 'r', encoding='utf-8').readlines()
    new_lines = []
    i = 0
    added = 0
    
    while i < len(lines):
        line = lines[i]
        new_lines.append(line)
        
        topic = get_topic(line)
        if topic:
            # Find paragraph content after this subsubsection
            i += 1
            para_lines = []
            while i < len(lines):
                nxt = lines[i]
                if nxt.strip().startswith(r'\subsubsection') or nxt.strip().startswith(r'\section') or nxt.strip().startswith(r'\subsection'):
                    break
                if nxt.strip().startswith('% ---'):
                    break
                para_lines.append(nxt)
                i += 1
            
            if para_lines:
                # Find first sentence end: after first 。 or ）
                first_line = para_lines[0]
                insert_idx = -1
                for pat in ['。\\', '）：\\', ')\\']:
                    pos = first_line.find(pat)
                    if pos != -1:
                        insert_idx = pos + len(pat)
                        break
                if insert_idx == -1:
                    # Fallback: after first 。
                    pos = first_line.find('。')
                    if pos != -1:
                        insert_idx = pos + 1
                
                if insert_idx != -1:
                    before = first_line[:insert_idx]
                    after = first_line[insert_idx:]
                    
                    # Insert prediction box
                    pred_text = generate_prediction(topic)
                    new_lines.append('\n')
                    new_lines.append('\t\\begin{tcolorbox}[colback=yellow!5, title=\\textit{读者行为预测}]\n')
                    new_lines.append(f'\t\t{pred_text}\n')
                    new_lines.append('\t\\end{tcolorbox}\n')
                    new_lines.append('\n')
                    new_lines.append(after)
                    new_lines.extend(para_lines[1:])
                    added += 1
                    continue
                else:
                    # Couldn't find sentence boundary, append prediction then continue
                    new_lines.append('\n')
                    new_lines.append('\t\\begin{tcolorbox}[colback=yellow!5, title=\\textit{读者行为预测}]\n')
                    new_lines.append(f'\t\t{generate_prediction(topic)}\n')
                    new_lines.append('\t\\end{tcolorbox}\n')
                    new_lines.append('\n')
                    added += 1
                    continue
        i += 1
    
    open(filepath, 'w', encoding='utf-8').writelines(new_lines)
    return added

if __name__ == '__main__':
    base = Path('.')
    target = base / 'part3_systematic_expansion/chapter3_politics_power_structure.tex'
    count = process_file(target)
    print(f"Added {count} prediction boxes to chapter3")
