# Step 1: Convert \textbf{段落N（whyN：主题）}：\\ to \subsubsection*{段落N：主题}
def replace_paragraph_header(m):
    num = m.group(1)  # 段落号
    # Extract topic after whyN： 
    full = m.group(0)
    # Extract the topic between ： and ） 
    topic_match = re.search(r'why\d+：(.+?)）', full)
    topic = topic_match.group(1) if topic_match else ""
    return f'\t\\subsubsection*{{段落{num}：{topic}}}'

